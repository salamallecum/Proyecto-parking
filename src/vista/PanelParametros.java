    //Metodo del boton exportar Backup
    public void ExportarDB(){
        
        try {
            String archivo = null;
            String ruta = null;
            
            JFC_exportarBackup.setVisible(true);                        
            int result = JFC_exportarBackup.showSaveDialog(null);
            
            if(result == JFileChooser.APPROVE_OPTION){
               
                archivo = JFC_exportarBackup.getSelectedFile().toString().concat(".sql");
                ruta = JFC_exportarBackup.getSelectedFile().getPath();
                
                File file = new File(archivo); 

                if(file.exists()){
                   Object[] options = { "Si", "No" };
                   int opcao = JOptionPane.showOptionDialog(null,"Este archivo ya existe.¿Quiere sobre escribir este Archivo?","¡¡¡Atención!!!",
                   JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null,options, options[0]);

                   if (opcao == JOptionPane.YES_OPTION) {                
                        Runtime bck = Runtime.getRuntime();   
                        bck.exec("C:/xampp/mysql/bin/mysqldump.exe --host=localhost --user=root --password= --result="+ruta+"-file="+archivo+" --databases bd_sistemaparking");  
                        JOptionPane.showMessageDialog(null, "¡Respaldo reemplazado!", "Generar Copia Seguridad", 1);

                    }
                }else{
                    Runtime bck = Runtime.getRuntime();   
                    bck.exec("C:/xampp/mysql/bin/mysqldump.exe --host=localhost --user=root --password= --result-file="+archivo+" --databases bd_sistemaparking");  
                    JOptionPane.showMessageDialog(null, "¡Respaldo  exitoso!", "Generar Copia Seguridad", 1);  

                }
            }
        } catch (HeadlessException | IOException e) {
            JOptionPane.showMessageDialog(null, e, "Error!", 2);
        } 
    }
